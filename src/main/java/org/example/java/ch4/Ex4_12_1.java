package org.example.java.ch4;

public class Ex4_12_1 {

  public static void main(String[] args){

    //for
    for(int i=1; i<=10; i++){
      System.out.printf("%d ", i);
    }
    System.out.println();

    //while
    int i=1;
    while(i<=10){
      System.out.printf("%d ", i);
      i++;
    }

  }

}
