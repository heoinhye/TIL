package org.example.java.ch4;

public class Ex4_8 {

  public static void main(String[] args){
    for(int i=0; i<5; i++){
      /*
      * 1) 초기화: i=0
      * 반복문에 사용될 변수를 초기화. 처음에 한번만 수행.
      * 2) 조건식: i<5
      * 조건식이 참이면 반복을 계속. 거짓이면 반복을 중단.
      * 3) 증감식: i++
      * 반복문을 제어하는 변수의 값을 증감.
      * */
      System.out.println("I CAN DO IT!");
    }
  }

}
