package org.example.java.ch4;

public class Ex4_7 {

  public static void main(String[] args){
    Double d1 = Math.random();
    System.out.println(d1); //0.0 ~ 1.0

    Double d2 = Math.random()*3;
    System.out.println(d2); //0.0 ~ 3.0

    int i1 = (int)Math.random()*3+1;
    System.out.println(i1); //0 ~ 4

  }

}
